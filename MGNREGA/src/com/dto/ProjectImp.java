package com.dto;

public class ProjectImp {
	private int pid;
	private String pname;
	private double pcost;
	private int pduration;
	private int eid;
	private int gid;
	
	public ProjectImp(int pid, String pname, double pcost, int pduration, int eid, int gid) {
		this.pid = pid;
		this.pname = pname;
		this.pcost = pcost;
		this.pduration = pduration;
		this.eid = eid;
		this.gid = gid;
	}
	
	
}
